% Визначення точок x і значень f(x)
x = [1, 2, 3, 4, 5];
f = [0, 1, 2, 1, 2];

% Обчислення наближеної похідної для f'(1) за допомогою правої різниці
f_prime_1 = (f(2) - f(1)) / (x(2) - x(1));

% Обчислення наближеної похідної для f'(3) за допомогою центральної різниці
f_prime_3 = (f(4) - f(2)) / (x(4) - x(2));

% Вивід результатів
disp(f_prime_1);
disp(f_prime_3);

